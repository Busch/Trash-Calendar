function getRequest() {
    var req = false;
    try{
        // most browsers
        req = new XMLHttpRequest();
    } catch (e){
        // IE
        try{
            req = new ActiveXObject("Msxml2.XMLHTTP");
        } catch (e) {
            // try an older version
            try{
                req = new ActiveXObject("Microsoft.XMLHTTP");
            } catch (e){
                return false;
            }
        }
    }
    return req;
}

function Abbonieren() {

  document.getElementById("absenden").style.display = 'none';
  document.getElementById("strasse_text").style.display = 'none';
  document.getElementById("muellarten_text").style.display = 'none';
  document.getElementById("abbonieren").style.display = 'none';
  document.getElementById("hausmuell").style.display = 'none';
  document.getElementById("hausmuell_text").style.display = 'none';
  document.getElementById("gelbersack").style.display = 'none';
  document.getElementById("gelbersack_text").style.display = 'none';
  document.getElementById("bioabfall").style.display = 'none';
  document.getElementById("bioabfall_text").style.display = 'none';
  document.getElementById("blauetonne").style.display = 'none';
  document.getElementById("blauetonne_text").style.display = 'none';
  document.getElementById("vereinssammlung").style.display = 'none';
  document.getElementById("vereinssammlung_text").style.display = 'none';
  document.getElementById("gruenabfuhr").style.display = 'none';
  document.getElementById("gruenabfuhr_text").style.display = 'none';

  var stadt_temp = document.getElementById("Stadt");
  var stadt = stadt_temp.options[stadt_temp.selectedIndex].value;
  var strasse_temp = document.getElementById("Strasse");
  var strasse = strasse_temp.options[strasse_temp.selectedIndex].value;

  if (document.getElementById('hausmuell').checked == true) { var hausmuell = "1"; } else { var hausmuell = "0"; }
  if (document.getElementById('gelbersack').checked == true) { var gelbersack = "1"; } else { var gelbersack = "0"; }
  if (document.getElementById('bioabfall').checked == true) { var bioabfall = "1"; } else { var bioabfall = "0"; }
  if (document.getElementById('blauetonne').checked == true) { var blauetonne = "1"; } else { var blauetonne = "0"; }
  if (document.getElementById('vereinssammlung').checked == true) { var vereinssammlung = "1"; } else { var vereinssammlung = "0"; }
  if (document.getElementById('gruenabfuhr').checked == true) { var gruenabfuhr = "1"; } else { var gruenabfuhr = "0"; }

  var ajax = getRequest();
  ajax.onreadystatechange = function(){
      if(ajax.readyState == 4){
          document.getElementById('abbonieren').innerHTML = "<a href=" + ajax.responseText + "><b>Zum Kalender hinzufügen</b></a>";
      }
  }
  ajax.open("GET", "script.php?bezirk=" + stadt + "&strasse=" + strasse + "&hausmuell=" + hausmuell + "&notify_hausmuell=13&gelbersack=" + gelbersack + "&notify_gelbersack=13&bioabfall=" + bioabfall + "&notify_bioabfall=13&blauetonne=" + blauetonne + "&notify_blauetonne=13&vereinssammlung=" + vereinssammlung + "&notify_vereinssammlung=13&gruenabfuhr=" + gruenabfuhr + "&notify_gruenabfuhr=13", true);
  ajax.send(null);
  document.getElementById("abbonieren").style.display = 'block';
}

